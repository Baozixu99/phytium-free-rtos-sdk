static void usbd_event_handler(uint8_t busid, uint8_t event)
{
    switch (event) {
        case USBD_EVENT_RESET:
            break;
        case USBD_EVENT_CONNECTED:
            break;
        case USBD_EVENT_DISCONNECTED:
            break;
        case USBD_EVENT_RESUME:
            break;
        case USBD_EVENT_SUSPEND:
            break;
        case USBD_EVENT_CONFIGURED:
            break;
        case USBD_EVENT_SET_REMOTE_WAKEUP:
            break;
        case USBD_EVENT_CLR_REMOTE_WAKEUP:
            break;

        default:
            break;
    }
}

#define BLOCK_SIZE  512
#define BLOCK_COUNT 100000

typedef struct
{
    uint8_t BlockSpace[BLOCK_SIZE];
} BLOCK_TYPE;

static BLOCK_TYPE mass_block[BLOCK_COUNT] = {0};

void usbd_msc_get_cap(uint8_t busid, uint8_t lun, uint32_t *block_num, uint32_t *block_size)
{
    *block_num = BLOCK_COUNT; //Pretend having so many buffer,not has actually.
    *block_size = BLOCK_SIZE;
}
int usbd_msc_sector_read(uint8_t busid, uint8_t lun, uint32_t sector, uint8_t *buffer, uint32_t length)
{
    if (sector < BLOCK_COUNT)
        memcpy(buffer, mass_block[sector].BlockSpace, length);
    return 0;
}

int usbd_msc_sector_write(uint8_t busid, uint8_t lun, uint32_t sector, uint8_t *buffer, uint32_t length)
{
    if (sector < BLOCK_COUNT)
        memcpy(mass_block[sector].BlockSpace, buffer, length);
    return 0;
}

static struct usbd_interface intf0;

int msc_ram_init(uint8_t busid, uint32_t reg_base)
{
    usbd_desc_register(busid, msc_ram_descriptor);
    usbd_add_interface(busid, usbd_msc_init_intf(busid, &intf0, MSC_OUT_EP, MSC_IN_EP));

    return usbd_initialize(busid, reg_base, usbd_event_handler);
}

int FPUsb2MassStorageExample(u32 id)
{
    int ret = msc_ram_init(id, usb_dc_get_register_base(id));

    if (ret == 0)
    {
        printf("%s@%d: PUSB2 mass storage example [success] !!! \r\n", __func__, __LINE__);
    }
    else
    {
        printf("%s@%d: PUSB2 mass storage example [failure] !!!, ret = %d \r\n", __func__, __LINE__, ret);
        return -1;
    }

    return 0;
}

void FPUsb2MassStorageExampleEnd(u32 id)
{
    usbd_deinitialize(id);
}